    private void configureTable() {
        Casestable.setRowHeight(25);
        Casestable.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        tableModel.setColumnIdentifiers(new String[]{"Investigation ID", "FIR ID", "Case Started", "Status"});
        Casestable.setModel(tableModel);
        Casestable.getColumnModel().getColumn(0).setPreferredWidth(120);
        Casestable.getColumnModel().getColumn(1).setPreferredWidth(100);
        Casestable.getColumnModel().getColumn(2).setPreferredWidth(100);
        Casestable.getColumnModel().getColumn(3).setPreferredWidth(100);
    }

    private void populateTable() {
        List<Investigation> investigations = csvHandler.loadInvestigations();
        System.out.println("Loaded " + investigations.size() + " investigations.");
        if (investigations.isEmpty()) {
            JOptionPane.showMessageDialog(this, "No investigations found in CSV.", "Info", JOptionPane.INFORMATION_MESSAGE);
        }
        List<Object[]> data = new java.util.ArrayList<>();
        SimpleDateFormat sdf = new SimpleDateFormat("MM/dd/yyyy");
        for (Investigation inv : investigations) {
            System.out.println("Investigation ID: " + inv.getInvestigationId() + ", Status: " + inv.getStatus());
            if (inv.getStatus() != null && inv.getStatus().equalsIgnoreCase("Completed")) {
                data.add(new Object[]{
                    inv.getInvestigationId(),
                    inv.getFirId(),
                    sdf.format(inv.getCreationDate()),
                    inv.getStatus()
                });
                System.out.println("Added row for INV: " + inv.getInvestigationId());
            }
        }
        tableModel.setDataVector(data.toArray(new Object[0][]), new String[]{"Investigation ID", "FIR ID", "Case Started", "Status"});
        System.out.println("Rows after setDataVector: " + tableModel.getRowCount());
        if (tableModel.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "No completed cases found.", "Info", JOptionPane.INFORMATION_MESSAGE);
        }
        Casestable.repaint();
    }
    
