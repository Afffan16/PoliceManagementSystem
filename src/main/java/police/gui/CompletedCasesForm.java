    private void ViewDetailsbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ViewDetailsbtnActionPerformed
            int selectedRow = Casestable.getSelectedRow();
            if (selectedRow >= 0) {
                String investigationId = (String) Casestable.getValueAt(selectedRow, 0);
                Investigation investigation = csvHandler.findInvestigationById(investigationId);
                if (investigation != null)
                {
                    new DisplayCaseDetails(investigation,loggedInUsername).setVisible(true);
                    dispose();
                }
            } 
            else 
            {
                JOptionPane.showMessageDialog(this, "Please select a case.", "No Case Selected", JOptionPane.WARNING_MESSAGE);
            }
    }//GEN-LAST:event_ViewDetailsbtnActionPerformed

