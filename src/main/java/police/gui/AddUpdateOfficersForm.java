    private void okbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okbtnActionPerformed
        String officerId = officerIDtxt.getText();
        String username = usernametxt.getText();
        String password = passwordtxt.getText();
        String role = roletxt.getText();
        String name = nametxt.getText();
        String badgeNo = badgeNotext.getText();
        String email = emailtxt.getText();
        String phone = phoneNotxt.getText();
        String serviceYearsStr = serviceYearstxt.getText();
        String casesSolvedStr = casesSolvedtxt.getText();
        String successPercentageStr = successPercentagetxt.getText();


        if (username.isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Username is required.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (password.isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Password is required.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (role.isEmpty()) 
        {
            JOptionPane.showMessageDialog(this, "Role is required.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }


        boolean isValid = FormValidator.validateOfficerForm(
            nametxt, lblerrname,
            emailtxt, lblerrAddress, 
            phoneNotxt, lblerrcontact,
            serviceYearstxt, lblerrdate, 
            casesSolvedtxt, lblerrtime,
            successPercentagetxt, lblerrCrimeType 
        );
        if (!isValid)
        {
            return;
        }

        int serviceYears;
        int casesSolved;
        double successPercentage;
        try 
        {
            serviceYears = Integer.parseInt(serviceYearsStr);
            casesSolved = Integer.parseInt(casesSolvedStr);
            successPercentage = Double.parseDouble(successPercentageStr);
            if (successPercentage < 0 || successPercentage > 100) 
            {
                JOptionPane.showMessageDialog(this, "Success Percentage must be between 0 and 100.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }
        catch (NumberFormatException e)
        {
            JOptionPane.showMessageDialog(this, "Please enter valid numeric values for Service Years, Cases Solved, and Success Percentage.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try 
        {
            String grade = csvHandler.calculateGrade(successPercentage);
            Officer newOfficer = new Officer(officerId, username, password, role, name, badgeNo, email, phone,
                serviceYears, casesSolved, successPercentage, grade, imagePath);
            if (isAddMode) 
            {
                csvHandler.writeCSV(newOfficer);
                JOptionPane.showMessageDialog(this, "Officer Added Successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
            } 
            else 
            {
                csvHandler.updateOfficer(newOfficer);
                JOptionPane.showMessageDialog(this, "Officer Updated Successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
            }
            parent.loadOfficers();
            dispose();
        } 
        catch (IOException e)
        {
            JOptionPane.showMessageDialog(this, "Error saving officer: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_okbtnActionPerformed

