    private void populateFields()
    {
        if (!isAddMode && officer != null) 
        {
            officerIDtxt.setText(officer.getOfficerId());
            badgeNotext.setText(officer.getBadgeNumber());
            usernametxt.setText(officer.getUsername());
            passwordtxt.setText(officer.getPassword());
            roletxt.setText(officer.getRole());
            nametxt.setText(officer.getName());
            emailtxt.setText(officer.getEmail());
            phoneNotxt.setText(officer.getPhone());
            serviceYearstxt.setText(String.valueOf(officer.getServiceYears()));
            casesSolvedtxt.setText(String.valueOf(officer.getCasesSolved()));
            successPercentagetxt.setText(String.valueOf(officer.getSuccessPercentage()));
            imagePath = officer.getImagePath(); 
        } 
        else 
        {
            String newOfficerId = generateOfficerId();
            officerIDtxt.setText(newOfficerId);
            badgeNotext.setText(newOfficerId);
        }
        officerIDtxt.setEditable(false);
        badgeNotext.setEditable(false);
}

private String generateOfficerId() 
{
    int count = csvHandler.getOfficerCount() + 1;
    return String.format("BDG-%03d", count);
}


    
    
    
